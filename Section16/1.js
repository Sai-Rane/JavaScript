// Synchronous code
// Synchronous code is executed line by line
// Each line waits for previous line to finish
// Disadvantage is Long running operations block code execution

// Asynchronous code
// Asynchronous code is executed after a task that runs in background finishes
// Asynchronous code is non blocking
// Exceution doesnt wait for an Asynchronous task to finish its work

